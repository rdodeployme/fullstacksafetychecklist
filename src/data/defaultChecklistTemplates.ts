function template(
  partial: Omit<ChecklistTemplate, "id" | "sections"> & { items: string[] },
): ChecklistTemplate {
  return {
    ...partial,
    id: partial.slug,
    sections: [
      {
        id: `${partial.slug}-main`,
        title: partial.createsIssue ? "Fault details" : "Safety check",
        displayOrder: 1,
        items: partial.items.map((prompt, index) => ({
          id: `${partial.slug}-${index + 1}`,
          prompt,
          displayOrder: index + 1,
          requiresIssueOnFail: true,
        })),
      },
    ],
  };
}

