export function getFallbackGroups() {
  const groups = new Map<string, ChecklistTemplate>();

  for (const template of defaultChecklistTemplates) {
    if (!groups.has(template.groupId)) {
      groups.set(template.groupId, template);
    }
  }

  return Array.from(groups.values()).map((template) => ({
    id: template.groupId,
    heading: template.groupTitle,
    description: template.groupDescription,
    icon: template.groupIcon,
    variant: template.groupVariant,
  }));
}
